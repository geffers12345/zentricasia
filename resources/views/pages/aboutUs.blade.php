<script>
    $(document).on('click', '#btnSend', function (e) {
        e.preventDefault();

        validate();

        if($('#errors').text() == '') {
            $.ajax({
                url: "{{ url("api/saveComment") }}",
                type: "POST",
                data: {
                    email: $('#email').val(),
                    comment: $('#comment').val()
                }, success: function () {
                    swal('Sent!', 'Comment Successfully Sent', 'success');
                    setTimeout(function () {
                        window.location.reload();
                    }, 3000);
                }
            });
        }
    });

    function validate() {

        $('#errors').text('');
        var errors = [];

        if($('#email').val() == '') {
            errors.push('Email cannot be empty');
        }

        if($('#comment').val() == '') {
            errors.push('Comment cannot be empty');
        }

        $.each(errors, function (id, key) {
            $('#errors').append('<li>' + key + '</li>');
        });
    }
</script>